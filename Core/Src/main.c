/**
 * @file    main.c
 * @brief   NUCLEO-G474RE + MPU-6050 — accel/gyro readout over UART
 *
 * Hardware connections (Arduino header / morpho):
 * ┌─────────────────┬──────────────────────────────────────────────┐
 * │ MPU-6050 pin    │ NUCLEO-G474RE pin                            │
 * ├─────────────────┼──────────────────────────────────────────────┤
 * │ VCC             │ 3.3 V  (CN6 pin 4)                           │
 * │ GND             │ GND    (CN6 pin 6)                           │
 * │ SCL             │ PB8  – Arduino D15 / CN5 pin 10  (I2C1_SCL) │
 * │ SDA             │ PB9  – Arduino D14 / CN5 pin 9   (I2C1_SDA) │
 * │ AD0             │ GND  → I2C address 0x68 (default)            │
 * │ INT             │ not connected (polling mode)                  │
 * └─────────────────┴──────────────────────────────────────────────┘
 *
 * UART output is routed through the ST-LINK Virtual COM Port (USART2,
 * PA2/PA3) at 115200 8N1 — open any serial terminal to see the data.
 *
 * CubeMX / CubeIDE peripheral settings required:
 *   • I2C1  : Standard Mode (100 kHz), PB8=SCL, PB9=SDA
 *   • USART2: Asynchronous, 115200 baud, 8N1 (ST-LINK VCP)
 *   • SYS   : Timebase source = SysTick
 *
 * Build: STM32CubeIDE with STM32CubeG4 HAL package.
 */

/* ------------------------------------------------------------------ */
/* Includes                                                             */
/* ------------------------------------------------------------------ */
#include "main.h"
#include "mpu6050.h"
#include <stdio.h>
#include <string.h>

/* ------------------------------------------------------------------ */
/* Private peripheral handles (generated by CubeMX / filled manually)  */
/* ------------------------------------------------------------------ */
I2C_HandleTypeDef  hi2c1;
UART_HandleTypeDef huart2;

/* ------------------------------------------------------------------ */
/* Private function prototypes                                          */
/* ------------------------------------------------------------------ */
static void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_I2C1_Init(void);
static void MX_USART2_UART_Init(void);

/* Redirect printf to USART2 */
static void uart_print(const char *str);

/* ------------------------------------------------------------------ */
/* main                                                                 */
/* ------------------------------------------------------------------ */
int main(void)
{
    /* HAL init, clock config, peripheral init */
    HAL_Init();
    SystemClock_Config();
    MX_GPIO_Init();
    MX_USART2_UART_Init();
    MX_I2C1_Init();

    uart_print("\r\n=== NUCLEO-G474RE + MPU-6050 ===\r\n");

    /* Initialise MPU-6050 (AD0 = GND → address 0x68) */
    MPU6050_Status_t status = MPU6050_Init(&hi2c1, MPU6050_I2C_ADDR_LOW);
    if (status == MPU6050_WRONG_ID)
    {
        uart_print("[ERROR] WHO_AM_I mismatch — check wiring and AD0 pin.\r\n");
        Error_Handler();
    }
    else if (status != MPU6050_OK)
    {
        uart_print("[ERROR] I2C communication failed — check pull-ups and connections.\r\n");
        Error_Handler();
    }

    uart_print("[OK] MPU-6050 initialised.\r\n\r\n");
    uart_print("Accel[g]              Gyro[deg/s]           Temp[C]\r\n");
    uart_print("Ax       Ay       Az  Gx       Gy       Gz\r\n");
    uart_print("-------- -------- --- -------- -------- --------\r\n");

    MPU6050_Data_t data;
    char line[128];

    while (1)
    {
        if (MPU6050_ReadAll(&hi2c1, MPU6050_I2C_ADDR_LOW, &data) == MPU6050_OK)
        {
            /* Format: fixed-width columns, 3 decimal places */
            snprintf(line, sizeof(line),
                     "%+7.3f  %+7.3f  %+7.3f  "
                     "%+8.2f  %+8.2f  %+8.2f  "
                     "%6.2f\r\n",
                     data.accel_x, data.accel_y, data.accel_z,
                     data.gyro_x,  data.gyro_y,  data.gyro_z,
                     data.temp_c);
            uart_print(line);
        }
        else
        {
            uart_print("[WARN] Read failed — retrying...\r\n");
        }

        HAL_Delay(100); /* 10 Hz print rate */
    }
}

/* ------------------------------------------------------------------ */
/* UART helper                                                          */
/* ------------------------------------------------------------------ */
static void uart_print(const char *str)
{
    HAL_UART_Transmit(&huart2, (const uint8_t *)str, (uint16_t)strlen(str),
                      HAL_MAX_DELAY);
}

